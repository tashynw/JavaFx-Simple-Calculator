package com.example.calculator;

import javafx.application.Application;
import javafx.fxml.FXMLLoader;
import javafx.scene.Scene;
import javafx.scene.image.Image;
import javafx.stage.Stage;

import java.io.IOException;

public class HelloApplication extends Application {
    @Override
    public void start(Stage stage) throws IOException {
        FXMLLoader fxmlLoader = new FXMLLoader(HelloApplication.class.getResource("calculatorDesign.fxml"));
        Scene scene = new Scene(fxmlLoader.load());
        //320,240
        stage.setTitle("Tashyn Simple Calculator");
        Image icon = new Image("C:\\Users\\Tashyn Wallace\\Downloads\\calculator.png");
        stage.getIcons().add(icon);
        stage.setScene(scene);
        stage.show();
    }

    public static void main(String[] args) {
        launch();
    }
}